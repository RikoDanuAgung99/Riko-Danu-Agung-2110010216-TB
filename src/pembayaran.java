    private void combo(){
        try {
            Statement st = (Statement) koneksi.getConnection().createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM data_pelanggan");
            while(rs.next()){
                cbidpelanggan.addItem(rs.getString("id_pelanggan"));
                cbnama.addItem(rs.getString("nama"));
                cbtujuan.addItem(rs.getString("tujuan_wisata"));
            }
        } catch (Exception e) {
        }
    }
    
    public void table() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("ID TRANSAKSI");
        tbl.addColumn("ID PELANGGAN");
        tbl.addColumn("NAMA");
        tbl.addColumn("TUJUAN WISATA");
        tbl.addColumn("HARGA TIKET");
        
        try {
            Statement st = (Statement) koneksi.getConnection().createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM data_transaksi");
            
            while(rs.next()){
                tbl.addRow(new Object[]{
                    rs.getInt("id_transaksi"),
                    rs.getInt("id_pelanggan"),
                    rs.getString("nama"),
                    rs.getString("tujuan_wisata"),
                    rs.getString("harga_tiket")
                });
                tbltransaksi.setModel(tbl);
            }
            JOptionPane.showMessageDialog(null, "Koneksi ke database berhasil");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "koneksi ke database gagal" + e.getMessage());
        }
        
      
    }

