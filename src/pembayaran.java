    private void tbltransaksiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbltransaksiMouseClicked
        int baris = tbltransaksi.rowAtPoint(evt.getPoint());
        String id_transaksi = tbltransaksi.getValueAt(baris, 0).toString(); idtransaksi.setText(id_transaksi);
        String id_pelanggan = tbltransaksi.getValueAt(baris, 0).toString(); cbidpelanggan.setSelectedItem(id_pelanggan);
        String nama_pelanggan = tbltransaksi.getValueAt(baris, 1).toString(); cbnama.setSelectedItem(nama_pelanggan);
        String tujuan_wisata = tbltransaksi.getValueAt(baris, 2).toString(); cbtujuan.setSelectedItem(tujuan_wisata);
        String harga_tiket = tbltransaksi.getValueAt(baris, 3).toString(); hargatiket.setText(harga_tiket);
    }//GEN-LAST:event_tbltransaksiMouseClicked

